#include <iostream>
using namespace std;

/** Receives 2 integers and returns the difference **/

int integerSubtraction(int a,int b){
  int diff;
  diff = a - b;
  return diff;
}

/** Receives 2 floats and returns the difference **/

float floatSubtraction(float a,float b){
  float diff;
  diff = a+b;
  return diff;
}


